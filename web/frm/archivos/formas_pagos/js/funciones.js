function validarFormulario() {
    var valor = true;
    if ($("#nombre_forma_pago").val().trim() === "") {
        valor = false;
        $("#mensajes").html("Forma de Pago no puede estar vacio.");
        $("#nombre_forma_pago").focus();
    }

    return valor;
}

function limpiarFormulario() {
    $("#id_forma_pago").val("");
    $("#nombre_forma_pago").val("");
    
}