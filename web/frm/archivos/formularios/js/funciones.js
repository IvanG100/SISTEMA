function SoloNumeros(evt) {
    if (window.event) {//asignamos el valor de la tecla a keynum
        keynum = evt.keyCode; //IE
    } else {
        keynum = evt.which; //FF
    }
    //comprobamos si se encuentra en el rango numérico y que teclas no recibirá.
    if ((keynum > 47 && keynum < 58) || keynum === 8 || keynum === 13 || keynum === 6) {
        return true;
    } else {
        alert("Solo ingrese numeros");
        return false;
    }
}

function validarFormulario() {
    var valor = true;
    if ($("#nombre_formulario").val().trim() === "") {
        valor = false;
        $("#mensajes").html("Nombre no puede estar vacio.");
        $("#nombre_formulario").focus();
    }
    if ($("#codigo_formulario").val().trim() === "") {
        valor = false;
        $("#mensajes").html("Codigo no puede estar vacio.");
        $("#codigo_formulario").focus();
    }
    
    if ($("#id_menu").val().trim() === "" || $("#id_menu").val().trim() === "0") {
        valor = false;
        $("#mensajes").html("Menu no puede estar vacio.");
        $("#ruc_cliente").focus();
    }
    
    

    return valor;
}