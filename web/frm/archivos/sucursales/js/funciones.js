function validarFormulario() {
    var valor = true;
    if ($("#nombre_sucursal").val().trim() === "") {
        valor = false;
        $("#mensajes").html("Sucursal no puede estar vacio.");
        $("#nombre_sucursal").focus();
    }

    return valor;
}

function limpiarFormulario() {
    $("#id_sucursal").val("");
    $("#nombre_sucursal").val("");
    
}
